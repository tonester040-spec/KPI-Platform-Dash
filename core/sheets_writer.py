#!/usr/bin/env python3
"""
core/sheets_writer.py
KPI Platform — writes enriched data back to Google Sheets.

Updates three tabs each pipeline run:
  CURRENT          → Clear + rewrite with current week (13 rows)
  STYLISTS_CURRENT → Clear + rewrite with current week stylists
  ALERTS           → Clear + rewrite with AI-generated alerts
"""

import os
import json
import base64
import logging
import datetime
from pathlib import Path

from google.oauth2 import service_account
from googleapiclient.discovery import build

log = logging.getLogger(__name__)

SCOPES = ["https://www.googleapis.com/auth/spreadsheets"]


def _build_service(config: dict):
    raw_b64 = os.environ.get("GOOGLE_SERVICE_ACCOUNT_JSON", "").strip()
    if not raw_b64:
        raise EnvironmentError("GOOGLE_SERVICE_ACCOUNT_JSON env var is not set.")
    sa_json = base64.b64decode(raw_b64).decode("utf-8")
    sa_info = json.loads(sa_json)
    creds = service_account.Credentials.from_service_account_info(sa_info, scopes=SCOPES)
    return build("sheets", "v4", credentials=creds)


def _clear_and_write(service, sheet_id: str, tab_range: str, rows: list[list]):
    """Clear a range then write new rows."""
    service.spreadsheets().values().clear(
        spreadsheetId=sheet_id,
        range=tab_range,
    ).execute()

    service.spreadsheets().values().update(
        spreadsheetId=sheet_id,
        range=tab_range,
        valueInputOption="USER_ENTERED",
        body={"values": rows},
    ).execute()


# ─── CURRENT tab ──────────────────────────────────────────────────────────────

def write_current(service, config: dict, locations: list[dict], dry_run: bool = False):
    """Rewrite the CURRENT tab with this week's location snapshot."""
    if dry_run:
        log.info("DRY RUN: skipping CURRENT tab write (%d rows)", len(locations))
        return

    sheet_id = config["sheet_id"]
    rows = []
    for loc in locations:
        rows.append([
            loc.get("loc_name", ""),
            loc.get("week_ending", ""),
            loc.get("platform", ""),
            loc.get("guests", 0),
            loc.get("total_sales", 0),
            loc.get("service", 0),
            loc.get("product", 0),
            loc.get("product_pct", 0),
            loc.get("ppg", 0),
            loc.get("pph", 0),
            loc.get("avg_ticket", 0),
            loc.get("prod_hours", 0),
            loc.get("wax_count", 0),
            loc.get("wax", 0),
            loc.get("wax_pct", 0),
            loc.get("color", 0),
            loc.get("color_pct", 0),
            loc.get("treat_count", 0),
            loc.get("treat", 0),
            loc.get("treat_pct", 0),
        ])

    _clear_and_write(service, sheet_id, "CURRENT!A2:T", rows)
    log.info("CURRENT tab: wrote %d location rows", len(rows))


# ─── STYLISTS_CURRENT tab ─────────────────────────────────────────────────────

def write_stylists_current(service, config: dict, stylists: list[dict], dry_run: bool = False):
    """Rewrite STYLISTS_CURRENT with this week's stylist snapshot."""
    if dry_run:
        log.info("DRY RUN: skipping STYLISTS_CURRENT tab write (%d rows)", len(stylists))
        return

    sheet_id = config["sheet_id"]
    rows = []
    for s in stylists:
        # Get current week (last entry)
        week = s["weeks"][-1] if s.get("weeks") else ""
        rows.append([
            week,
            s.get("name", ""),
            s.get("loc_name", ""),
            s.get("loc_id", ""),
            s.get("status", "active"),
            s.get("tenure", 0),
            s.get("cur_pph", 0),
            s.get("cur_rebook", 0),
            s.get("cur_product", 0),
            s.get("cur_ticket", 0),
            s["services"][-1] if s.get("services") else 0,
            s["color"][-1] if s.get("color") else 0,
        ])

    _clear_and_write(service, sheet_id, "STYLISTS_CURRENT!A2:L", rows)
    log.info("STYLISTS_CURRENT tab: wrote %d stylist rows", len(rows))


# ─── ALERTS tab ───────────────────────────────────────────────────────────────

def _build_alerts(locations: list[dict], network: dict, ai_cards: dict) -> list[list]:
    """
    Generate alert rows from enriched location data and AI cards.
    Returns list of [Location Name, Alert Type, Severity, Message, Week Ending, Generated By]
    """
    rows = []
    week = network.get("week_ending", "")

    for loc in locations:
        name = loc["loc_name"]
        flag = loc.get("flag", "solid")
        card = ai_cards.get("location_cards", {}).get(name, "")

        if flag == "star":
            rows.append([name, "RECOGNITION", "LOW", card or f"Strong performance this week.", week, "KPI AI"])
        elif flag == "watch":
            rows.append([name, "WATCH", "HIGH", card or f"Performance below network average — coach attention recommended.", week, "KPI AI"])

        # Product % alert
        prod_delta = loc.get("product_pct_delta")
        if prod_delta is not None and prod_delta <= -3.0:
            rows.append([
                name, "PRODUCT_DROP", "MEDIUM",
                f"Product % dropped {abs(prod_delta):.1f} points vs last week ({loc['product_pct']:.1f}%).",
                week, "KPI AI"
            ])

        # PPH alert
        pph_delta_pct = loc.get("pph_delta_pct")
        if pph_delta_pct is not None and pph_delta_pct <= -5.0:
            rows.append([
                name, "PPH_DROP", "MEDIUM",
                f"PPH dropped {abs(pph_delta_pct):.1f}% vs last week (${loc['pph']:.2f}).",
                week, "KPI AI"
            ])

    # Add coach briefing as a network-level alert
    briefing = ai_cards.get("coach_briefing", "")
    if briefing:
        # Just add first 250 chars as a summary alert
        summary = briefing[:250].replace("\n", " ").strip()
        rows.append(["ALL LOCATIONS", "COACH BRIEFING", "LOW", summary, week, "KPI AI"])

    return rows


def write_alerts(
    service,
    config: dict,
    locations: list[dict],
    network: dict,
    ai_cards: dict,
    dry_run: bool = False,
):
    """Rewrite the ALERTS tab with this week's AI-generated alerts."""
    alert_rows = _build_alerts(locations, network, ai_cards)

    if dry_run:
        log.info("DRY RUN: ALERTS tab would get %d rows:", len(alert_rows))
        for row in alert_rows:
            log.info("  [%s] %s — %s", row[2], row[0], row[3][:60])
        return

    sheet_id = config["sheet_id"]
    _clear_and_write(service, sheet_id, "ALERTS!A2:F", alert_rows)
    log.info("ALERTS tab: wrote %d alert rows", len(alert_rows))


# ─── Main entry ───────────────────────────────────────────────────────────────

def write_all(config: dict, data: dict, ai_cards: dict, dry_run: bool = False):
    """Write CURRENT, STYLISTS_CURRENT, and ALERTS in one call."""
    service = _build_service(config) if not dry_run else None
    write_current(service, config, data["locations"], dry_run)
    write_stylists_current(service, config, data["stylists"], dry_run)
    write_alerts(service, config, data["locations"], data["network"], ai_cards, dry_run)
    log.info("Sheets write complete (dry_run=%s)", dry_run)
