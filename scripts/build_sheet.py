# ─── Auth / Config ────────────────────────────────────────────────────────────

def load_config() -> dict:
    if not CONFIG_PATH.exists():
        raise FileNotFoundError(
            f"Config file not found: {CONFIG_PATH}\n"
            "Create config/customers/karissa_001.json with a 'sheet_id' key."
        )
    return json.loads(CONFIG_PATH.read_text())


def build_sheets_service(config: dict):
    raw_b64 = os.environ.get("GOOGLE_SERVICE_ACCOUNT_JSON", "").strip()
    if not raw_b64:
        raise EnvironmentError(
            "GOOGLE_SERVICE_ACCOUNT_JSON environment variable is not set.\n"
            "Export the base64-encoded service account JSON before running."
        )
    try:
        sa_json = base64.b64decode(raw_b64).decode("utf-8")
        sa_info = json.loads(sa_json)
    except Exception as exc:
        raise ValueError(
            f"Could not decode GOOGLE_SERVICE_ACCOUNT_JSON: {exc}"
        ) from exc
    creds = service_account.Credentials.from_service_account_info(
        sa_info, scopes=SCOPES
    )
    return build("sheets", "v4", credentials=creds)


# ─── Spreadsheet helpers ──────────────────────────────────────────────────────

def fetch_spreadsheet(service, spreadsheet_id: str) -> dict:
    return service.spreadsheets().get(spreadsheetId=spreadsheet_id).execute()


def ensure_tabs_exist(service, spreadsheet_id: str, current_ss: dict) -> dict:
    """
    Create any missing tabs in a single batchUpdate call.
    Returns a refreshed spreadsheet dict (makes one extra GET only if needed).
    """
    existing_titles = {s["properties"]["title"] for s in current_ss["sheets"]}
    missing = [t for t in TABS if t not in existing_titles]
    if not missing:
        return current_ss

    add_requests = [
        {"addSheet": {"properties": {"title": title}}} for title in missing
    ]
    service.spreadsheets().batchUpdate(
        spreadsheetId=spreadsheet_id,
        body={"requests": add_requests},
    ).execute()
    print(f"      Created new tab(s): {', '.join(missing)}")
    return fetch_spreadsheet(service, spreadsheet_id)


def extract_sheet_meta(spreadsheet: dict) -> tuple[dict, dict]:
    """
    Returns (sheet_ids, existing_banding) dicts keyed by tab title.
      sheet_ids[title]       → int sheetId
      existing_banding[title] → list of bandedRangeId ints
    """
    sheet_ids       = {}
    existing_banding = {}
    for sheet in spreadsheet["sheets"]:
        title = sheet["properties"]["title"]
        sheet_ids[title] = sheet["properties"]["sheetId"]
        existing_banding[title] = [
            b["bandedRangeId"]
            for b in sheet.get("bandedRanges", [])
        ]
    return sheet_ids, existing_banding


# ─── Low-level request builders ───────────────────────────────────────────────

def _header_cell(text: str) -> dict:
    """Navy background, white bold text."""
    return {
        "userEnteredValue": {"stringValue": text},
        "userEnteredFormat": {
            "backgroundColor": C_NAVY,
            "textFormat": {
                "bold": True,
                "foregroundColor": C_WHITE,
            },
            "verticalAlignment": "MIDDLE",
        },
    }


def _plain_cell(text: str, bg: dict) -> dict:
    return {
        "userEnteredValue": {"stringValue": text} if text else {},
        "userEnteredFormat": {"backgroundColor": bg},
    }


def _empty_cell(bg: dict) -> dict:
    return {
        "userEnteredValue": {},
        "userEnteredFormat": {"backgroundColor": bg},
    }


def _colored_string_cell(text: str, bg: dict) -> dict:
    return {
        "userEnteredValue": {"stringValue": text},
        "userEnteredFormat": {"backgroundColor": bg},
    }


def _req_clear(sheet_id: int) -> dict:
    """Wipe all values and formats on the entire sheet."""
    return {
        "updateCells": {
            "range":  {"sheetId": sheet_id},
            "fields": "userEnteredValue,userEnteredFormat",
        }
    }


def _req_write_rows(sheet_id: int, row_index: int, rows: list) -> dict:
    return {
        "updateCells": {
            "start":  {"sheetId": sheet_id, "rowIndex": row_index, "columnIndex": 0},
            "rows":   rows,
            "fields": "userEnteredValue,userEnteredFormat",
        }
    }


def _req_freeze(sheet_id: int, rows: int = 1, cols: int = 0) -> dict:
    grid_props = {"frozenRowCount": rows}
    fields     = "gridProperties.frozenRowCount"
    if cols:
        grid_props["frozenColumnCount"] = cols
        fields += ",gridProperties.frozenColumnCount"
    return {
        "updateSheetProperties": {
            "properties": {
                "sheetId":      sheet_id,
                "gridProperties": grid_props,
            },
            "fields": fields,
        }
    }


def _req_banding(sheet_id: int, num_cols: int) -> dict:
    """
    Add alternating row banding starting at row index 1 (below the header).
    firstBandColor  → white  (rows 2, 4, 6 …)
    secondBandColor → #F8F8F8 (rows 3, 5, 7 …)
    endRowIndex omitted intentionally — covers the entire sheet.
    """
    return {
        "addBanding": {
            "bandedRange": {
                "range": {
                    "sheetId":        sheet_id,
                    "startRowIndex":  1,
                    "startColumnIndex": 0,
                    "endColumnIndex": num_cols,
                    # endRowIndex omitted → extends to end of sheet
                },
                "rowProperties": {
                    "firstBandColor":  C_WHITE,
                    "secondBandColor": C_LGRAY,
                },
            }
        }
    }


def _req_delete_banding(banding_id: int) -> dict:
    return {"deleteBanding": {"bandedRangeId": banding_id}}


def _req_col_widths(sheet_id: int, widths: list) -> list:
    return [
        {
            "updateDimensionProperties": {
                "range": {
                    "sheetId":    sheet_id,
                    "dimension":  "COLUMNS",
                    "startIndex": i,
                    "endIndex":   i + 1,
                },
                "properties": {"pixelSize": w},
                "fields":     "pixelSize",
            }
        }
        for i, w in enumerate(widths)
    ]


def _req_auto_resize(sheet_id: int, num_cols: int) -> dict:
    return {
        "autoResizeDimensions": {
            "dimensions": {
                "sheetId":    sheet_id,
                "dimension":  "COLUMNS",
                "startIndex": 0,
                "endIndex":   num_cols,
            }
        }
    }


def _req_basic_filter(sheet_id: int, num_cols: int) -> dict:
    return {
        "setBasicFilter": {
            "filter": {
                "range": {
                    "sheetId":        sheet_id,
                    "startRowIndex":  0,
                    "startColumnIndex": 0,
                    "endColumnIndex": num_cols,
                }
            }
        }
    }


# ─── Tab request builders ─────────────────────────────────────────────────────

def build_current_requests(sheet_id: int, stale_banding: list) -> list:
    """
    CURRENT tab — live weekly snapshot.
    Navy header, freeze row 1 + col A, alternating bands, auto-resize.
    """
    reqs = []
    reqs += [_req_delete_banding(bid) for bid in stale_banding]
    reqs.append(_req_clear(sheet_id))
    reqs.append(_req_write_rows(
        sheet_id, 0,
        [{"values": [_header_cell(h) for h in PERF_HEADERS]}],
    ))
    reqs.append(_req_freeze(sheet_id, rows=1, cols=1))
    reqs.append(_req_banding(sheet_id, len(PERF_HEADERS)))
    reqs.append(_req_auto_resize(sheet_id, len(PERF_HEADERS)))
    return reqs


def build_data_requests(sheet_id: int, stale_banding: list) -> list:
    """
    DATA tab — full historical record, append-only.
    Same header as CURRENT. Fixed column widths (no auto-resize).
    Filter on all columns.
    """
    reqs = []
    reqs += [_req_delete_banding(bid) for bid in stale_banding]
    reqs.append(_req_clear(sheet_id))
    reqs.append(_req_write_rows(
        sheet_id, 0,
        [{"values": [_header_cell(h) for h in PERF_HEADERS]}],
    ))
    reqs.append(_req_freeze(sheet_id, rows=1, cols=1))
    reqs.append(_req_banding(sheet_id, len(PERF_HEADERS)))
    reqs.append(_req_basic_filter(sheet_id, len(PERF_HEADERS)))
    reqs += _req_col_widths(sheet_id, DATA_COL_WIDTHS)
    return reqs


def build_goals_requests(
    sheet_id: int,
    stale_banding: list,
    location_names: list,
) -> list:
    """
    GOALS tab — annual targets per location.
    Row 1 header uses navy bg but col A1 text is red bold ("Admin Only").
    13 pre-populated location rows with blank goal cells.
    """
    reqs = []
    reqs += [_req_delete_banding(bid) for bid in stale_banding]
    reqs.append(_req_clear(sheet_id))

    # Build header row — all cells start navy/white ...
    header_row = {"values": [_header_cell(h) for h in GOALS_HEADERS]}

    # ... then we'll override A1 afterwards with red text.
    # Build the 13 location data rows with alternating backgrounds.
    data_rows = []
    for i, name in enumerate(location_names[:13]):
        bg = C_WHITE if i % 2 == 0 else C_LGRAY
        cells = [_plain_cell(name, bg)] + [
            _empty_cell(bg) for _ in range(len(GOALS_HEADERS) - 1)
        ]
        data_rows.append({"values": cells})

    reqs.append(_req_write_rows(sheet_id, 0, [header_row] + data_rows))

    # Override A1: navy bg, red bold text — admin warning.
    reqs.append({
        "updateCells": {
            "start": {"sheetId": sheet_id, "rowIndex": 0, "columnIndex": 0},
            "rows": [{
                "values": [{
                    "userEnteredValue": {
                        "stringValue": "GOALS — Admin Only. Do not edit."
                    },
                    "userEnteredFormat": {
                        "backgroundColor": C_NAVY,
                        "textFormat": {
                            "bold":            True,
                            "foregroundColor": C_RED,
                        },
                    },
                }]
            }],
            "fields": "userEnteredValue,userEnteredFormat",
        }
    })

    reqs.append(_req_freeze(sheet_id, rows=1))
    reqs += _req_col_widths(sheet_id, GOALS_COL_WIDTHS)
    return reqs


def build_alerts_requests(
    sheet_id: int,
    stale_banding: list,
    location_names: list,
    today: str,
) -> list:
    """
    ALERTS tab — AI-generated flags.
    3 sample rows pre-populated (one per severity level).
    Severity drives row background color: HIGH=red, MEDIUM=amber, LOW=green.
    """
    loc = location_names  # shorthand

    # Sample rows: [Location Name, Alert Type, Severity, Message, Week Ending, Generated By]
    sample_data = [
        [
            loc[0] if len(loc) > 0 else "Location 1",
            "RECOGNITION",
            "LOW",
            "Strong week — highest PPH in the network at $XX/hr",
            today,
            "KPI AI",
        ],
        [
            loc[1] if len(loc) > 1 else "Location 2",
            "WATCH",
            "MEDIUM",
            "Product % dropped 4 points vs last week. Coach attention recommended.",
            today,
            "KPI AI",
        ],
        [
            loc[2] if len(loc) > 2 else "Location 3",
            "GOAL_PACE",
            "HIGH",
            "Tracking 18% below annual sales goal pace. Needs recovery plan.",
            today,
            "KPI AI",
        ],
    ]

    rows = [{"values": [_header_cell(h) for h in ALERTS_HEADERS]}]
    for row_data in sample_data:
        bg    = SEVERITY_COLORS[row_data[2]]
        cells = [_colored_string_cell(str(v), bg) for v in row_data]
        rows.append({"values": cells})

    reqs = []
    reqs.append(_req_clear(sheet_id))
    reqs.append(_req_write_rows(sheet_id, 0, rows))
    reqs.append(_req_freeze(sheet_id, rows=1))
    reqs += _req_col_widths(sheet_id, ALERTS_COL_WIDTHS)
    return reqs


# ─── Main ─────────────────────────────────────────────────────────────────────

def main():
    print("╔══════════════════════════════════════════════════╗")
    print("║  KPI Sheet Builder — Karissa's Salon Network     ║")
    print("╚══════════════════════════════════════════════════╝")

    # ── 1. Load config
    print("\n[1/5] Loading config ...")
    config = load_config()

    spreadsheet_id = config.get("sheet_id", "").strip()
    if not spreadsheet_id or spreadsheet_id == "REPLACE_WITH_YOUR_GOOGLE_SHEET_ID":
        print(
            "\n❌  sheet_id is not set in config/customers/karissa_001.json.\n"
            "    Replace the placeholder value with your real Google Sheet ID.",
            file=sys.stderr,
        )
        sys.exit(1)

    raw_locations = config.get("locations", [])
    # Support both flat string list (legacy) and object list {id, name, platform}
    if raw_locations and isinstance(raw_locations[0], dict):
        location_names = [loc["name"] for loc in raw_locations]
    else:
        location_names = list(raw_locations)
    while len(location_names) < 13:
        location_names.append(f"Location {len(location_names) + 1}")
    location_names = location_names[:13]

    print(f"      Sheet ID  : {spreadsheet_id}")
    print(f"      Locations : {', '.join(location_names)}")

    # ── 2. Authenticate
    print("\n[2/5] Authenticating with Google Sheets API ...")
    service = build_sheets_service(config)
    print("      ✓ Authenticated")

    # ── 3. Ensure all 4 tabs exist
    print("\n[3/5] Ensuring all 4 tabs exist ...")
    spreadsheet = fetch_spreadsheet(service, spreadsheet_id)
    spreadsheet = ensure_tabs_exist(service, spreadsheet_id, spreadsheet)

    sheet_ids, existing_banding = extract_sheet_meta(spreadsheet)
    for tab in TABS:
        bcount = len(existing_banding.get(tab, []))
        note   = f"({bcount} existing banding range(s) will be replaced)" if bcount else ""
        print(f"      {tab:<10} sheetId={sheet_ids[tab]}  {note}")

    # ── 4. Build all requests
    today = datetime.date.today().strftime("%Y-%m-%d")
    print(f"\n[4/5] Building formatting requests (today = {today}) ...")

    all_requests = (
        build_current_requests(
            sheet_ids[TAB_CURRENT],
            existing_banding.get(TAB_CURRENT, []),
        )
        + build_data_requests(
            sheet_ids[TAB_DATA],
            existing_banding.get(TAB_DATA, []),
        )
        + build_goals_requests(
            sheet_ids[TAB_GOALS],
            existing_banding.get(TAB_GOALS, []),
            location_names,
        )
        + build_alerts_requests(
            sheet_ids[TAB_ALERTS],
            existing_banding.get(TAB_ALERTS, []),
            location_names,
            today,
        )
    )
    print(f"      {len(all_requests)} total API requests queued")

    # ── 5. Execute
    print("\n[5/5] Executing batchUpdate ...")
    service.spreadsheets().batchUpdate(
        spreadsheetId=spreadsheet_id,
        body={"requests": all_requests},
    ).execute()

    print(
        "\n✅  Sheet setup complete!\n"
        f"   https://docs.google.com/spreadsheets/d/{spreadsheet_id}\n"
    )


if __name__ == "__main__":
    try:
        main()
    except FileNotFoundError as exc:
        print(f"\n❌  Config error: {exc}", file=sys.stderr)
        sys.exit(1)
    except EnvironmentError as exc:
        print(f"\n❌  Auth error: {exc}", file=sys.stderr)
        sys.exit(1)
    except ValueError as exc:
        print(f"\n❌  Value error: {exc}", file=sys.stderr)
        sys.exit(1)
    except HttpError as exc:
        print(f"\n❌  Google API error: {exc}", file=sys.stderr)
        sys.exit(1)
