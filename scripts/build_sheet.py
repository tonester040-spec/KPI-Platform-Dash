# ─── Colors (Google Sheets API uses 0.0–1.0 float components) ─────────────────

def _rgb(r: int, g: int, b: int) -> dict:
    """Convert 0–255 int components to a Sheets API color dict."""
    return {"red": r / 255, "green": g / 255, "blue": b / 255}

