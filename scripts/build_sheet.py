"""
build_sheet.py
KPI Platform — Karissa's Salon Network (13 locations)

Sets up a Google Sheet with 4 tabs:
  CURRENT  – Live snapshot of most recent week. Cleared/rewritten every Monday.
  DATA     – Full historical record, append-only.
  GOALS    – Annual targets per location (admin-only, protected label).
  ALERTS   – AI-generated flags from most recent run. Cleared/rewritten every Monday.

Authentication:
  Service account JSON (base64-encoded) in GOOGLE_SERVICE_ACCOUNT_JSON env var.

Config:
  config/customers/karissa_001.json → { "sheet_id": "...", "locations": [...] }

Usage:
  python scripts/build_sheet.py
"""

